## Fundamentos de la Firma Electrónica en Chile

La firma electrónica se ha integrado en el marco jurídico chileno, promoviendo la autenticación digital y la validez de documentos electrónicos. Este avance es crucial para el desarrollo de procesos más eficientes y seguros en la gestión documental.

### Definición y naturaleza jurídica de la firma electrónica

La firma electrónica es un conjunto de datos electrónicos que permiten identificar al firmante en un documento digital. Su naturaleza jurídica radica en su capacidad para ofrecer firmas con validez legal, acorde con la normativa vigente, siempre que se cumplan ciertos requisitos de seguridad y autenticación.

### Ley 19.799 y su impacto en la validez legal

La implementación de la Ley 19.799 marca un hito en la legislación chilena, otorgando un marco que respalda el uso de firmas electrónicas. Esta ley asegura que los documentos firmados electrónicamente tienen la misma validez que aquellos firmados de manera manuscrita, siempre que se respeten las normas establecidas en cuanto a la seguridad y autenticidad de las firmas.

### Diferencias entre firma electrónica y firma manuscrita

Las firmas electrónicas y las manuscritas se diferencian en varios aspectos clave:

- **Proceso de creación:** La firma manuscrita se realiza físicamente, mientras que la firma electrónica es generada y almacenada digitalmente.
- **Verificación:** La firma electrónica emplea tecnologías para verificar la identidad del firmante, lo que puede ofrecer mayores niveles de seguridad que una firma manuscrita.
- **Almacenamiento y gestión:** Los documentos digitales con firma electrónica pueden ser almacenados y gestionados de manera más eficiente en comparación con los documentos en papel.

## Tipos de Firma Electrónica

Las firmas electrónicas se clasifican en dos categorías principales según su nivel de seguridad y la forma en que se emiten. Estas son la firma electrónica simple y la firma electrónica avanzada. A continuación, se detallan sus características, usos y requisitos.

### Firma Electrónica Simple (FES)

#### Características principales

La Firma Electrónica Simple (FES) es un símbolo o conjunto de datos que permite identificar al firmante de una forma básica. No requiere un nivel alto de seguridad ni la intervención de un proveedor acreditado. Su naturaleza flexible permite su uso en diversos contextos, aunque su respaldo legal es más limitado.

#### Usos comunes y limitaciones

Este tipo de firma se utiliza comúnmente para documentos de menor riesgo, como aceptaciones de términos y condiciones en sitios web, correos electrónicos, y otros trámites no críticos. Sin embargo, su principal limitación radica en ser más fácilmente impugnable en caso de disputa legal.

#### Seguridad y validez legal

La FES ofrece un nivel básico de seguridad que puede ser suficiente para documentos informales. Aunque es reconocida legalmente, su validez es menor en comparación con firmas más robustas en contextos judiciales o notariales.

### Firma Electrónica Avanzada (FEA)

#### Requisitos y emisión por prestador acreditado

La Firma Electrónica Avanzada (FEA) debe ser emitida por proveedores acreditados y requiere cumplir con estándares altos de autenticidad. Esto incluye un proceso de verificación de identidad para garantizar la seguridad del firmante.

#### Métodos de autenticación: certificados digitales y biometría

Los métodos de autenticación de la FEA incluyen el uso de certificados digitales y sistemas biométricos, como huellas dactilares o reconocimiento facial, que aseguran la identidad del firmante de manera sólida.

#### Aplicaciones en documentos legales y contratos

La FEA es crucial para la firma de documentos legales, contratos laborales, y escritos que requieren un alto nivel de seguridad. Gracias a su robustez, tiene la misma validez legal que una firma manuscrita en situaciones críticas.

#### Seguridad y protección frente a impugnaciones

Su estructura de seguridad proporciona un sólido respaldo frente a posibles impugnaciones. El uso de múltiples factores de autenticación hace que la FEA sea confiable y difícil de falsificar.

