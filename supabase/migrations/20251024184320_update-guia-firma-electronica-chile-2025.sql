## Preguntas Frecuentes sobre Firma Electrónica en Chile

### ¿La firma electrónica tiene la misma validez que una firma manuscrita?

Sí, según la Ley 19.799, los documentos firmados electrónicamente tienen la misma validez legal que aquellos firmados de manera manuscrita, siempre que cumplan con los requisitos de seguridad y autenticación establecidos.

### ¿Cuánto tiempo toma obtener una firma electrónica en Chile?

El proceso puede variar según el proveedor, pero generalmente toma entre 24 y 48 horas desde la verificación de identidad hasta la emisión del certificado digital.

### ¿Puedo usar mi firma electrónica en cualquier documento?

La firma electrónica simple puede usarse en la mayoría de documentos no críticos. Para documentos legales importantes, contratos y escrituras, se requiere una Firma Electrónica Avanzada (FEA) emitida por un proveedor acreditado.

### ¿Qué pasa si pierdo mi certificado digital?

En caso de pérdida o robo, debes contactar inmediatamente a tu proveedor de servicios de certificación para revocar el certificado y solicitar uno nuevo, evitando así posibles usos fraudulentos.

### ¿La firma electrónica es segura?

Sí, especialmente la Firma Electrónica Avanzada (FEA) utiliza múltiples capas de seguridad incluyendo certificados digitales, biometría y encriptación, haciéndola altamente segura y difícil de falsificar.

### ¿Cuánto cuesta obtener una firma electrónica en Chile?

Los costos varían según el proveedor y el tipo de firma. Las firmas electrónicas simples pueden ser gratuitas, mientras que las avanzadas tienen costos anuales que oscilan entre $20.000 y $80.000 CLP dependiendo del proveedor y los servicios incluidos.

